const titleCase = (s) =>
  s.replace(/^_*(.)|_+(.)/g, (s, c, d) => c ? c.toUpperCase() : ' ' + d.toUpperCase())


const { Column } = Table;


const OrderViewTable = () => {
  const [searchText, setSearchText] = useState('');
  const [searchedColumn, setSearchedColumn] = useState('');
  const [OrderData, setOrderData] = useState([])
  const searchInput = useRef(null);



  useEffect(() => {

    axios.get('/api/admin/allorders' ).then(response => {
      const results = response.data.results
      console.log(results);
      let orderResults = results?.map(data => {
        return {
          "key": data.id,
          "product_name": data.product_name,
          "order_qty": data.order_qty,
          "ordered_by": data.full_name,
          "ordered_date": data.ordered_date,
          "payment_method": data.payment_method_name,
          "Payment_completed": data.is_payment_completed,
          "delivered_date": data.delivered_date,
          "delivery_fee": data.delivery_fee,
          "total_amount": data.total_amount,
          "delivery_person_name": data.delivery_person_name,
          "delivery_address": data.delivery_address,
          "landmark": data.landmark,
          "postal_code": data.postal_code,
          "city": data.city,
          "contact_number": data.contact_number
        }
      })
      setOrderData(orderResults)

    })

  }, [])


  useEffect(() => {
    console.log(OrderData);
  }, [OrderData])





  const handleSearch = (selectedKeys, confirm, dataIndex) => {
    confirm();
    setSearchText(selectedKeys[0]);
    setSearchedColumn(dataIndex);
  };
  const handleReset = (clearFilters) => {
    clearFilters();
    setSearchText('');
  };
  const getColumnSearchProps = (dataIndex) => ({
    filterDropdown: ({ setSelectedKeys, selectedKeys, confirm, clearFilters, close }) => (
      <div
        style={{
          padding: 8,
        }}
        onKeyDown={(e) => e.stopPropagation()}
      >
        <Input
          ref={searchInput}
          placeholder={`Search ${dataIndex}`}
          value={selectedKeys[0]}
          onChange={(e) => setSelectedKeys(e.target.value ? [e.target.value] : [])}
          onPressEnter={() => handleSearch(selectedKeys, confirm, dataIndex)}
          style={{
            marginBottom: 8,
            display: 'block',
          }}
        />
        <Space>
          <Button
            type="primary"
            onClick={() => handleSearch(selectedKeys, confirm, dataIndex)}
            icon={<SearchOutlined />}
            size="small"
            style={{
              width: 90,
            }}
          >
            Search
          </Button>
          <Button
            onClick={() => clearFilters && handleReset(clearFilters)}
            size="small"
            style={{
              width: 90,
            }}
          >
            Reset
          </Button>
          <Button
            type="link"
            size="small"
            onClick={() => {
              confirm({
                closeDropdown: false,
              });
              setSearchText(selectedKeys[0]);
              setSearchedColumn(dataIndex);
            }}
          >
            Filter
          </Button>
          <Button
            type="link"
            size="small"
            onClick={() => {
              close();
            }}
          >
            close
          </Button>
        </Space>
      </div>
    ),
    filterIcon: (filtered) => (
      <SearchOutlined
        style={{
          color: filtered ? '#1890ff' : undefined,
        }}
      />
    ),
    onFilter: (value, record) =>
      record[dataIndex].toString().toLowerCase().includes(value.toLowerCase()),
    onFilterDropdownOpenChange: (visible) => {
      if (visible) {
        setTimeout(() => searchInput.current?.select(), 100);
      }
    },
    render: (text) =>
      searchedColumn === dataIndex ? (
        <Highlighter
          highlightStyle={{
            backgroundColor: '#ffc069',
            padding: 0,
          }}
          searchWords={[searchText]}
          autoEscape
          textToHighlight={text ? text.toString() : ''}
        />
      ) : (
        text
      ),
  });




  const columns = [
    {
      title: 'Name',
      dataIndex: 'product_name',
      key: 'Product_name',
      width: '30%',
      ...getColumnSearchProps('product_name'),
    },

    {
      title: 'Order QTY',
      dataIndex: 'order_qty',
      key: 'order_qty',
      width: '20%',
      ...getColumnSearchProps('age'),
    },
    {
      title: 'Address',
      dataIndex: 'address',
      key: 'address',
      ...getColumnSearchProps('address'),
      sorter: (a, b) => a.address.length - b.address.length,
      sortDirections: ['descend', 'ascend'],
    },
  ];



  const Col = ["product_name", "order_qty", "ordered_by", "ordered_date", "payment_method", "Payment_completed", "delivered_date", "delivery_fee", "total_amount", "delivery_person_name", "delivery_address", "landmark", "postal_code", "city", "contact_number"].map(c => {
    return {
      title: titleCase(c),
      dataIndex: c,
      width: '30%',
      key: c,
      ...getColumnSearchProps(c)
    }
  })

  Col.push({
    title: titleCase('action'),
    dataIndex: 'action',
    width: '30%',
    key: 'action',
    render: (_, record) => <Link href={`/admin/orders/${record.key}`}><a onClick={()=>console.log(record)}>View</a></Link>
  })






  return <Table columns={Col} dataSource={OrderData} size='large'  loading={OrderData.length <=0 }/>
};
export default OrderViewTable;